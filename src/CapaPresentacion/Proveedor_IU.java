    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        try {
            if (txtRuc.getText().length() > 0) {

                int aviso = JOptionPane.showConfirmDialog(rootPane, "Estar seguro de eliminar");
                if (aviso == 0) {

                    ProveedorBD oProveedorBD = new ProveedorBD();

                    String dni = txtRuc.getText();

                    boolean rpta = oProveedorBD.eliminarProveedor(dni);
                    if (rpta) {
                        reportar();
                        limpiar();
                        btnRegistrar.setEnabled(true);
                        btnModificar.setEnabled(false);
                        btnEliminar.setEnabled(false);

                        txtRuc.requestFocus();

                    } else {
                        error("Tienes problemas al eliminar");
                    }

                } else {
                    advertencia("Ingresar Ruc");
                    txtRuc.requestFocus();
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

