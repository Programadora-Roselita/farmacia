    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        if (txtRuc.getText().length() > 0) {

            String ruc = txtRuc.getText();
            ProveedorBD objeto_ProductoBD = new ProveedorBD();
            DefaultTableModel tabla_temporal;
            tabla_temporal = objeto_ProductoBD.buscarProveedor(ruc);

            if (tabla_temporal.getRowCount() > 0) {

                tabla_reporte_proveedor.setModel(tabla_temporal);
                txtRuc.setText(tabla_temporal.getValueAt(0, 0).toString());
                txtRazonSocial.setText(tabla_temporal.getValueAt(0, 1).toString());
                txtRepresentante.setText(tabla_temporal.getValueAt(0, 2).toString());
                txtDireccion.setText(tabla_temporal.getValueAt(0, 3).toString());
                txtCorreo.setText(tabla_temporal.getValueAt(0, 4).toString());
                txtPaginaWeb.setText(tabla_temporal.getValueAt(0, 5).toString());
                txtCelular.setText(tabla_temporal.getValueAt(0, 6).toString());

                btnRegistrar.setEnabled(false);
                btnModificar.setEnabled(true);
                btnEliminar.setEnabled(true);

            } else {
                error("No se encontro datos");
                txtRuc.requestFocus();

            }

        } else {
            advertencia("Ingrese ruc");
            txtRuc.requestFocus();

        }


    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:

        try {

            if (txtRuc.getText().length() > 0) {
                if (txtRazonSocial.getText().length() > 0) {
                    if (txtCorreo.getText().length() > 0) {
                        if (txtDireccion.getText().length() > 0) {
                            if (txtCelular.getText().length() > 0) {
                                if (txtDireccion.getText().length() > 0) {
                                    if (txtPaginaWeb.getText().length() > 0) {

                                        Proveedor objeto_usuario = new Proveedor();
                                        ProveedorBD objeto_usuarioBD = new ProveedorBD();

                                        objeto_usuario.setProvRuc(txtRuc.getText().toUpperCase().trim());
                                        objeto_usuario.setProvRazonSocial(txtRazonSocial.getText().toUpperCase().trim());
                                        objeto_usuario.setProvRepresentante(txtRepresentante.getText().toUpperCase().trim());
                                        objeto_usuario.setProvDireccion(txtDireccion.getText().toUpperCase().trim());
                                        objeto_usuario.setProvCorreo(txtCorreo.getText().toUpperCase().trim());
                                        objeto_usuario.setProvWeb(txtPaginaWeb.getText().trim());
                                        objeto_usuario.setProvCelular(txtCelular.getText().trim());

                                        boolean rpta = objeto_usuarioBD.modificarProveedor(objeto_usuario);
                                        if (rpta) {
                                            exito("Todo ok...");
                                            reportar();
                                            limpiar();

                                            btnRegistrar.setEnabled(true);
                                            btnModificar.setEnabled(false);
                                            btnEliminar.setEnabled(false);
                                            txtRuc.requestFocus();

                                        } else {
                                            error("Tienes problemas al modificar...");
                                        }

                                    } else {
                                        advertencia("Ingrese una pagina web...");
                                        txtPaginaWeb.requestFocus();
                                    }

                                } else {
                                    advertencia("Ingrese una direccion...");
                                    txtDireccion.requestFocus();
                                }

                            } else {
                                advertencia("Ingrese celular...");
                                txtCelular.requestFocus();
                            }

                        } else {
                            advertencia("Ingrese una direccion...");
                            txtDireccion.requestFocus();
                        }

                    } else {
                        advertencia("Ingrese correo...");
                        txtCorreo.requestFocus();
                    }

                } else {
                    advertencia("Ingrese razon social...");
                    txtRazonSocial.requestFocus();
                }

            } else {
                advertencia("Ingrese ruc...");
                txtRuc.requestFocus();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

